package com.example.demo.repository;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class Hero {

    @Id
    private int id;
    private String name;

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
